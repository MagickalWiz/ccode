#include <iostream>
using namespace std;

int main(){
	int i;
	double d;
	float f;
	char c;
	string s;
	bool done;
	
	i = 123456789;
	d = 3.14159268;
	f = 1.23;
	c = 'A';
	s = "ABCDEFGHIJKLMNOPQRSTUVWXYZ";
	done = true;
	
	cout << i <<"\n";
	cout << d <<"\n";
	cout << f <<"\n";
	cout << c <<"\n";
	cout << s <<"\n";
	cout << done <<"\n";
	
	done = false;
	
	cout << done <<"\n";
}
